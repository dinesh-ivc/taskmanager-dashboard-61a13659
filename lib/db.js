// This file provides a connection pool for PostgreSQL using node-pg library
// Since we're using Supabase client directly, this file may not be used in the current 
// implementation but kept as a generic db utility

import { Pool } from 'pg';

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: process.env.NODE_ENV === 'production' ? { rejectUnauthorized: false } : false,
});

export const query = async (text, params) => {
  const start = Date.now();
  const res = await pool.query(text, params);
  const duration = Date.now() - start;
  console.log('Executed query', { text, duration, rows: res.rowCount });
  return res;
};

// Export connection for direct usage
export { pool };